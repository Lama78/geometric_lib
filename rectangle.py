def area(a, b):
    '''
        Принимает целые числа: a и b - стороны прямоугольника
        Возвращает значение площади - целое число
        a(7) b(2) = a * b = 14
    '''
    return a * b
def perimeter(a, b):
    '''
        Принимает целые числа: a и b - стороны прямоугольника
        Возвращает значение периметра - целое число
        a(7) b(10) = 2*(a+b) = 34
    '''
    return 2 * (a + b)